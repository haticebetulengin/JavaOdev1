package oopIntroOdev;

public class Course {
	int id; 
	String courseName; 
	String courseDetails;
	String priceDetails;
	
	public Course() {
		
	}
	
	public Course(int id, String courseName, String courseDetails, String priceDetails) {
		this.id = id;
		this.courseName = courseName;
		this.courseDetails = courseDetails;
		this.priceDetails = priceDetails;
		
	}

}
